// Нормализация названий игр для сравнения "это та же самая игра?".
// Игроки вводят названия вручную в роллбоксе, и в одной и той же игре
// могут быть разные регистры, пробелы, римские/арабские цифры, кавычки,
// дефисы и т.п. Без нормализации бонус «первым прошёл» (+2) выдавался
// почти всем.
//
// Правила:
//   - trim,
//   - всё в нижний регистр,
//   - схлопываем подряд пробелы в один,
//   - убираем большую часть пунктуации (.,!?:;'"`«»"-—_),
//   - римские цифры I..X → арабские (для случаев типа "Dark Souls III" / "3"),
//   - схлопываем "the " в начале (тоже частая вариация).

const ROMAN_MAP: Record<string, string> = {
  i: "1", ii: "2", iii: "3", iv: "4", v: "5",
  vi: "6", vii: "7", viii: "8", ix: "9", x: "10",
};

export function normalizeGameTitle(raw: string): string {
  let s = raw.trim().toLowerCase();

  // Убираем пунктуацию (оставляем буквы, цифры, пробелы и & как разделитель).
  s = s.replace(/[.,!?:;'"`«»“”\-—_()[\]{}]/g, " ");

  // Схлопываем пробелы
  s = s.replace(/\s+/g, " ").trim();

  // "the " в начале — отбрасываем
  if (s.startsWith("the ")) s = s.slice(4);

  // Римские → арабские (только отдельными словами).
  s = s
    .split(" ")
    .map((tok) => (ROMAN_MAP[tok] ? ROMAN_MAP[tok] : tok))
    .join(" ");

  return s;
}
